package org.tns.operator;
//Program demonstrate the LOgical Operator
public class LogicalIOperatorDemo {

	@SuppressWarnings("unused")
	public static void main(String[] args) {
		System.out.println(3!=7 && 3>=5);
		System.out.println(3!=7 || 3>=5);
		System.out.println(!(3!=7 || 3>=5));
		

	}

}
